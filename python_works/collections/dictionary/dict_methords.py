# Class dict( )

    # def get(key) - return values of corresponding key ,if key not exists returns none
    # def pop(key) - remove an iten from dictionary
    # def keys()   -  return keys
    # def values() -  return values
    # def items()  -  return keys and values correspondly



fruits = {'a':'apple','b':'banana','c':'cherry','d':'dragon-fruit'}


# def items()
for k,v in fruits.items():

    print(k,v)


# def keys()
for k in fruits.keys():

    print(k)


# def values()
for v in fruits.values():

    print(v)

